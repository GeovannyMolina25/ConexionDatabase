    private void tblAlumnoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlumnoMouseClicked
        
        try{
            int fila = tblAlumno.getSelectedRow();
            String id= tblAlumno.getValueAt(fila, 0).toString();
            Connection con = Conexion.getConexion();

             PreparedStatement ps;
             ResultSet rs;
             ps = con.prepareStatement("SELECT cedula_alu,nombre_alu,sexo_alu,fecha_nac_alu,direccion_alu,telefono_alu,email_alu,observacion_alu FROM alumno WHERE codigo_alu=?");
             ps.setString(1, id);
             rs=ps.executeQuery();
             
             while(rs.next()){
                 txtCodigo.setText(String.valueOf(id));
                 txtCedula.setText(rs.getString("cedula_alu"));
                 txtNombre.setText(rs.getString("nombre_alu"));
                 txtFechaNac.setText(rs.getString("fecha_nac_alu"));
                 txtDireccion.setText(rs.getString("direccion_alu"));
                 txtTelefono.setText(rs.getString("telefono_alu"));
                 txtEmail.setText(rs.getString("email_alu"));
                 txtaObservaciones.setText(rs.getString("observacion_alu"));
                 if(rs.getString("sexo_alu").equals("MASCULINO")){
                     rbMasculino.setSelected(true);
                 }else if(rs.getString("sexo_alu").equals("FEMENINO")){
                     rbFemenino.setSelected(true);
                 } 
             }
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_tblAlumnoMouseClicked

