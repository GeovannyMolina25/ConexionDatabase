    private void cargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tblAlumno.getModel();
        modeloTabla.setRowCount(0);
        
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        try{
            Connection con = Conexion.getConexion();

             ps = con.prepareStatement("SELECT codigo_alu,cedula_alu,nombre_alu,sexo_alu,fecha_nac_alu,direccion_alu,telefono_alu,email_alu,observacion_alu FROM alumno");
             rs=ps.executeQuery();
             rsmd = rs.getMetaData();
             columnas = rsmd.getColumnCount();
             while(rs.next()){
                 Object[] fila = new Object[columnas];
                 for(int indice=0; indice<columnas;indice++){
                     fila[indice] = rs.getObject(indice + 1);
                 }
                 modeloTabla.addRow(fila);
             }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    
    
    }
